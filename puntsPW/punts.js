function sort(attr){
	if(!attr){attr='lifetime';}
	Jugadors.sort((a,b)=>{
		if(a[attr]<b[attr]) return 1;
		if(a[attr]>b[attr]) return -1;
		return 0;
	});
	dibuixa();
}

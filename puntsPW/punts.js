function dibuixa() {
	div_id='chart';
	var DATA=[
		['Jugador','Lifetime points','Season points'],
	];
	Jugadors.forEach((j,i)=>{DATA.push([(i+1)+". "+j.nom,j.Lifetime,j.Season]);});
	var data=google.visualization.arrayToDataTable(DATA);

	var options = {
		legend:{position:'top'},
		fontSize:"11",
		chartArea:{
			top:50,
		},
		width:"100%",
		height:3000,
		bars:'horizontal',
		bar:{
			groupWidth:"70%",
		},
		isStacked:true,
	};
	var chart=new google.visualization.BarChart(document.getElementById(div_id));
	chart.draw(data,options);
}
//ordena i dibuixa
function sort(attr){
	if(!attr){attr='Lifetime';}
	Jugadors.sort((a,b)=>{
		if(a[attr]<b[attr]) return 1;
		if(a[attr]>b[attr]) return -1;
		return 0;
	});
	dibuixa();
}
